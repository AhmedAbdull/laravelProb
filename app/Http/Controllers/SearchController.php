public function search(Request $request){
    $searchData = $request->input('searchData');
    //echo $searchData;
   $products = DB::table('ah_products')->leftJoin('ah_productstranslateinfo','ah_products.code','=','ah_productstranslateinfo.code')->where('ah_productstranslateinfo.lang','=','en')->orderBy('orderNum')->where('ah_products.name','like','%'. $searchData . '%')->get();
    return view('mainShop.search')->with('products',$products);
}
